# - 숫자 자료형
# 1. 정수형 : 소수점이 없는 수
print(1, 3, 0 , -1, sep="")
#, sep="" => 공백 문자 설정

# 2. 실수형 : 소수점이 있는 수
print(1.1, 3.1, 0, -1.4)

# - 문자 자료형
print('hihihi', end='')
# end='' => 문자열 공백 설정, 문자열 사이 입력할 경우 나란히 입력됨
print("hello")
print('"hihihihi" is english')

# - 불린 자료형
# true or false
print(True)
print(False)