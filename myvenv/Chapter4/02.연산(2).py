# 1. 비교 연산
print("- 비교연산")
print(2 > 3) # False
print(15 < 30) # True
print(1.5 >= 0) # True
print(3 <= 3) # True
print("팔팔팗" == "팔팔팔") # False
print("111111" != "111!111") # True

# 논리 연산
print("- 논리연산")
print(4 < 6 and 10 >= 10) # True and True -> True
print("포기" != "포기" or "할수있따" == "할수있따") # False or True -> True
print(not 5==5) # not True -> False

